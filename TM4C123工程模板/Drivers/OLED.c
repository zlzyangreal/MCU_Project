/**************************************
函数名:	void IIC_Send_Byte(uint8_t txd)
说明:	IIC发送一个字节
入口:	uint8_t	txd
出口:	无
备注:	无
**************************************/
void Write_IIC_Byte(uint8_t txd)
{
  uint8_t t;
  
  SDA_OUT();
  IIC_SCL_L;				// 拉低时钟开始数据传输
  for (t = 0; t < 8; t++)
  {
    if ((txd&0x80) >> 7)
    {
      IIC_SDA_H;
    }
    else
    {
      IIC_SDA_L;
    }
    txd <<= 1;
    //Delay_Us(1);
    IIC_SCL_H;
    //Delay_Us(1); 
    IIC_SCL_L;
  }
  IIC_Wait_Ack();
}

