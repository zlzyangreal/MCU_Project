/**********************************
函数名:	uint8_t IIC_Wait_Ack(void)
说明:	等待应答信号到来
入口:	无
出口:	uint8_t 1,接收应答失败
0,接收应答成功
备注:	无
**********************************/
uint8_t IIC_Wait_Ack(void)
{
  uint8_t ucErrTime = 0;
  SDA_IN();					// SDA设置为输入  
  IIC_SDA_H;
  //Delay_Us(1);		
  IIC_SCL_H;
  //Delay_Us(1);	 
  while (READ_SDA)			// SDA为高,等待IIC器件拉低
  {
    ucErrTime++;
    if (ucErrTime > 250)	//	40*250=1ms未答复,IIC发出停止信号
    {
      OLED_IIC_Stop();
      return 1;
    }
  }
  IIC_SCL_L;
  return 0;
}

